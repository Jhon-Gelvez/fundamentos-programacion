// 7. Calcular el doble y el triple

// inicio

// declaramos las variables
let numero, doble, triple;
// datos de ingreso
numero = parseInt(prompt("ingrese un numero"));

// operaciones
doble = numero * 2;
triple = numero * 3;

// datos de salida
alert("el doble del numero es " + doble);
alert("el triple del numero es " + triple);
// fin
