// 6 calcular perimetro de un cuadrado
// inicio

// declaramos las variables
let lado, perimetro;
// datos de ingreso
lado = parseInt(prompt("ingrese la longitud de un lado del cuadrado"));
// operaciones
perimetro = lado * 4;
// datos de salida
alert("perimetro " + perimetro);
